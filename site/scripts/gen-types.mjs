/**
 * gen-types.mjs
 * Reads schema files from ../schema/ (repo root) and compiles them into
 * TypeScript interfaces written to src/lib/types.ts.
 *
 * Run via: node scripts/gen-types.mjs
 * Invoked automatically by the prebuild / pretypecheck npm hooks.
 */

import { compileFromFile } from 'json-schema-to-typescript';
import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, resolve, join } from 'path';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Paths relative to this script (site/scripts/gen-types.mjs)
const SCHEMA_DIR = resolve(__dirname, '../../schema');
const OUT_FILE = resolve(__dirname, '../src/lib/types.ts');

const schemas = [
  { file: 'paper.schema.json',        bannerType: 'RmePaperEntry' },
  { file: 'datasets.schema.json',     bannerType: 'RmeDatasetCatalog' },
  { file: 'taxonomy.schema.json',     bannerType: 'RmeTaxonomyTree' },
  { file: 'reading-path.schema.json', bannerType: 'RmeReadingPath' },
];

const compileOptions = {
  bannerComment: '',
  unknownAny: false,
  enableConstEnums: false,
  style: { singleQuote: true },
};

async function main() {
  const parts = [
    '// THIS FILE IS AUTO-GENERATED by site/scripts/gen-types.mjs',
    '// DO NOT EDIT BY HAND — changes will be overwritten on next build.',
    '// Source: schema/*.schema.json',
    '',
  ];

  for (const { file } of schemas) {
    const schemaPath = join(SCHEMA_DIR, file);
    const ts = await compileFromFile(schemaPath, {
      ...compileOptions,
      cwd: SCHEMA_DIR,
    });
    parts.push(ts);
  }

  // Ensure output directory exists
  mkdirSync(dirname(OUT_FILE), { recursive: true });
  writeFileSync(OUT_FILE, parts.join('\n'), 'utf-8');
  console.log(`[gen-types] Written: ${OUT_FILE}`);
}

main().catch((err) => {
  console.error('[gen-types] Error:', err);
  process.exit(1);
});
